def swap(a,b):
    """ Swap the content of lists a and b

    >>> a = [1, 'two', 3]
    >>> b = [4, [5, 6]]
    >>> swap(a, b)
    >>> a
    [4, [5, 6]]
    >>> b
    [1, 'two', 3]
    """
    a[:], b[:] = list(a), list(b)

